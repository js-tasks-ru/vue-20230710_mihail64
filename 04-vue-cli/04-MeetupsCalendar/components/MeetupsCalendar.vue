<template>
  <div class="calendar-view">
    <div class="calendar-view__controls">
      <div class="calendar-view__controls-inner">
        <button
          class="calendar-view__control-left"
          type="button"
          aria-label="Previous month"
          @click="previousMonth"
        ></button>
        <div class="calendar-view__date">{{ selectedMonth }}</div>
        <button class="calendar-view__control-right" type="button" aria-label="Next month" @click="nextMonth"></button>
      </div>
    </div>

    <div class="calendar-view__grid">
      <div
        v-for="i in daysArray"
        :key="i"
        class="calendar-view__cell"
        :class="{ 'calendar-view__cell_inactive': !i.thisMonth }"
        tabindex="0"
      >
        <div class="calendar-view__cell-day">{{ i.day }}</div>
        <div class="calendar-view__cell-content">
          <a
            v-for="meetup in meetupsByDates[i.d]"
            :key="meetup.id"
            href="{{`/meetups/${meetup.id}`}}"
            class="calendar-event"
            >{{ meetup.title }}</a
          >
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import dayjs from 'dayjs';

export default {
  name: 'MeetupsCalendar',
  data() {
    return {
      selectedDate: new dayjs(),
    };
  },
  computed: {
    selectedMonth() {
      return this.selectedDate.toDate().toLocaleDateString(navigator.language, {
        month: 'long',
        year: 'numeric',
      });
    },
    firstDayOfMonth() {
      return this.selectedDate.startOf('month');
    },
    lastDayOfMonth() {
      return this.selectedDate.endOf('month');
    },
    lastDayOfPreviousMonth() {
      return this.selectedDate.subtract(1, 'months').endOf('month');
    },
    firstDayOfWeekIndex() {
      return this.firstDayOfMonth.day() === 0 ? 6 : this.firstDayOfMonth.day() - 1;
    },
    lastDayOfWeekIndex() {
      return this.lastDayOfMonth.day() === 0 ? 6 : this.lastDayOfMonth.day() - 1;
    },
    daysArray() {
      const daysArray = [];
      for (let i = this.firstDayOfWeekIndex - 1; i >= 0; i -= 1) {
        daysArray.push({
          day: this.lastDayOfPreviousMonth.subtract(i, 'day').date(),
          thisMonth: false,
          d: this.lastDayOfPreviousMonth.subtract(i, 'day').format('YYYY-MM-DD'),
        });
      }
      for (let i = 1; i <= this.lastDayOfMonth.date(); i += 1) {
        daysArray.push({ day: i, thisMonth: true, d: this.firstDayOfMonth.add(i - 1, 'day').format('YYYY-MM-DD') });
      }
      for (let i = 1; i <= 6 - this.lastDayOfWeekIndex; i += 1) {
        daysArray.push({
          day: i,
          thisMonth: false,
          d: this.firstDayOfMonth
            .add(1, 'month')
            .add(i - 1, 'day')
            .format('YYYY-MM-DD'),
        });
      }
      return daysArray;
    },
    meetupsByDates() {
      const meetupsByDates = {};
      this.meetups.forEach((meetup) => {
        const date = new dayjs(meetup.date);
        const dateFormatted = date.format('YYYY-MM-DD');
        if (!meetupsByDates[dateFormatted]) {
          meetupsByDates[dateFormatted] = [];
        }
        meetupsByDates[dateFormatted].push(meetup);
      });
      return meetupsByDates;
    },
  },
  methods: {
    nextMonth() {
      this.selectedDate = this.selectedDate.add(1, 'month');
    },
    previousMonth() {
      this.selectedDate = this.selectedDate.subtract(1, 'month');
    },
  },
  props: {
    meetups: {
      type: Array,
      required: true,
    },
  },
};
</script>

